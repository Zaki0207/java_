package com.careri.APLdrive;

public class GuidSignal {
    double target_speed = -999;
    double target_angle = -999;

    public double getTarget_speed() {
        return target_speed;
    }

    public void setTarget_speed(double target_speed) {
        this.target_speed = target_speed;
    }

    public double getTarget_angle() {
        return target_angle;
    }

    public void setTarget_angle(double target_angle) {
        this.target_angle = target_angle;
    }
}
